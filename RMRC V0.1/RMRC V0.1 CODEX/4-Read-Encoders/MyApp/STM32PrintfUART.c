static uint8_t dma_buf[UART_DMA_BUF_SIZE];

static volatile uint8_t tx_busy = 0;
static volatile uint32_t uart_drop_count = 0;


/* ============================
   Critical Section Helpers
   ============================ */

static inline uint32_t UART_SaveIRQ(void)
{
    uint32_t primask = __get_PRIMASK();
    __disable_irq();
    return primask;
}

static inline void UART_RestoreIRQ(uint32_t primask)
{
    if (!primask)
    {
        __enable_irq();
    }
}


/* ============================
   Ring Buffer Helpers
   ============================ */

static uint16_t RingBuffer_Next(uint16_t index)
{
    index++;
    if (index >= UART_BUFFER_SIZE)
    {
        index = 0;
    }
    return index;
}

static uint8_t RingBuffer_IsEmpty(void)
{
    return head == tail;
}

static uint8_t RingBuffer_IsFull(void)
{
    return RingBuffer_Next(head) == tail;
}

static uint8_t RingBuffer_WriteByte(uint8_t data)
{
    uint8_t result = 0;

    uint32_t irq = UART_SaveIRQ();

    if (!RingBuffer_IsFull())
    {
        uart_buffer[head] = data;
        head = RingBuffer_Next(head);
        result = 1;
    }
    else
    {
        uart_drop_count++;
        result = 0;
    }

    UART_RestoreIRQ(irq);

    return result;
}


/* ============================
   UART DMA Kick Function
   ============================ */

void UART_TX_Task(void)
{
    uint16_t count = 0;
    uint16_t temp_tail;
    uint16_t local_head;

    /*
     * ??? DMA ????? ??? ?? ??????? ??????? ???? ??.
     * ??? ???? ?????? tx_busy ?? ?????? 1 ??????? ?? ???? ?????? ???? ????? ????.
     */
    uint32_t irq = UART_SaveIRQ();

    if (tx_busy)
    {
        UART_RestoreIRQ(irq);
        return;
    }

    if (RingBuffer_IsEmpty())
    {
        UART_RestoreIRQ(irq);
        return;
    }

    tx_busy = 1;

    temp_tail = tail;
    local_head = head;

    UART_RestoreIRQ(irq);

    /*
     * ??????? ?? ?? ring buffer ?? dma_buf ??? ???????.
     * ????? ????? ???? IRQ ??????? ???? ???:
     * - writer ??? head ?? ????? ??????.
     * - tail ??? ?? ???? ???? ??? ??????.
     * - tx_busy ????? ??????? ??? ???? ?????? ?????? ???? ????? ???.
     */
    while ((count < UART_DMA_BUF_SIZE) && (temp_tail != local_head))
    {
        dma_buf[count++] = uart_buffer[temp_tail];

        temp_tail++;
        if (temp_tail >= UART_BUFFER_SIZE)
        {
            temp_tail = 0;
        }
    }

    /*
     * ???? ???? ????? ???? ?????
     */
    if (count == 0)
    {
        irq = UART_SaveIRQ();
        tx_busy = 0;
        UART_RestoreIRQ(irq);
        return;
    }

    /*
     * ???? ????? DMA
     */
    if (HAL_UART_Transmit_DMA(&huart1, dma_buf, count) == HAL_OK)
    {
        /*
         * ??? ??????? ???? dma_buf ??? ????????
         * ????????? tail ?? ??? ?????.
         */
        irq = UART_SaveIRQ();
        tail = temp_tail;
        UART_RestoreIRQ(irq);
    }
    else
    {
        /*
         * ??? DMA ?????? ?????? busy ?? ???? ?? ?? ????? ?????? ???? ???.
         */
        irq = UART_SaveIRQ();
        tx_busy = 0;
        UART_RestoreIRQ(irq);
    }
}


