/* ============================
   Optional Log Function
   ============================ */

void Log(const char *log)
{
    if (log == NULL)
    {
        return;
    }

    while (*log)
    {
        RingBuffer_WriteByte((uint8_t)*log);
        log++;
    }

    UART_TX_Task();
}


/* ============================
   Optional Debug Functions
   ============================ */

uint32_t UART_GetDropCount(void)
{
    return uart_drop_count;
}

void UART_ClearDropCount(void)
{
    uint32_t irq = UART_SaveIRQ();
    uart_drop_count = 0;
    UART_RestoreIRQ(irq);
}


